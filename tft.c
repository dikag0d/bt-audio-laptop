bool tft_init(void) {
    if (SDL_Init(SDL_INIT_VIDEO) < 0) {
        printf("SDL tidak bisa di-load! SDL_Error: %s\n", SDL_GetError());
        return false;
    }

    window = SDL_CreateWindow("TFT 240x240 Simulator",
                              SDL_WINDOWPOS_UNDEFINED, SDL_WINDOWPOS_UNDEFINED,
                              TFT_WIDTH * 2, TFT_HEIGHT * 2, SDL_WINDOW_SHOWN);
    if (window == NULL) {
        return false;
    }

    renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_ACCELERATED);
    if (renderer == NULL) {
        return false;
    }

    SDL_RenderSetLogicalSize(renderer, TFT_WIDTH, TFT_HEIGHT);
    SDL_SetRenderDrawColor(renderer, 0, 0, 0, 255);
    SDL_RenderClear(renderer);
    SDL_RenderPresent(renderer);

    return true;
}

void tft_close(void) {
    SDL_DestroyRenderer(renderer);
    SDL_DestroyWindow(window);
    SDL_Quit();
}

void tft_draw_pixel(int x, int y, uint8_t r, uint8_t g, uint8_t b) {
    if (x < 0 || x >= TFT_WIDTH || y < 0 || y >= TFT_HEIGHT) return;
    SDL_SetRenderDrawColor(renderer, r, g, b, 255);
    SDL_RenderDrawPoint(renderer, x, y);
}

void tft_fill_screen(uint8_t r, uint8_t g, uint8_t b) {
    SDL_SetRenderDrawColor(renderer, r, g, b, 255);
    SDL_RenderClear(renderer);
}

void tft_draw_rect(int x, int y, int w, int h, uint8_t r, uint8_t g, uint8_t b) {
    SDL_Rect rect = {x, y, w, h};
    SDL_SetRenderDrawColor(renderer, r, g, b, 255);
    SDL_RenderFillRect(renderer, &rect);
}

void tft_draw_char(int x, int y, char c, uint8_t r, uint8_t g, uint8_t b, int size) {
    if (c < 32 || c > 126) return;
    uint8_t* glyph = &font5x7[(c - 32) * 5];
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 8; j++) {
            if (glyph[i] & (1 << j)) {
                if (size == 1) {
                    tft_draw_pixel(x + i, y + j, r, g, b);
                } else {
                    tft_draw_rect(x + i * size, y + j * size, size, size, r, g, b);
                }
            }
        }
    }
}

void tft_draw_string(int x, int y, const char* str, uint8_t r, uint8_t g, uint8_t b, int size) {
    int cur_x = x;
    while (*str) {
        tft_draw_char(cur_x, y, *str, r, g, b, size);
        cur_x += 6 * size;
        str++;
    }
}

void tft_update(void) {
    SDL_RenderPresent(renderer);
}

bool tft_process_events(void) {
    SDL_Event e;
    while (SDL_PollEvent(&e) != 0) {
        if (e.type == SDL_QUIT) return false;
    }
    return true;
}
